package eu.hayde.box.template.xml;

/**
 *
 * @author senturk
 */
public class XMLException extends Exception {

	public XMLException() {
		super();
	}

	public XMLException(String message) {
		super(message);
	}

	public XMLException(String message, Throwable cause) {
		super(message, cause);
	}

	public XMLException(Throwable cause) {
		super(cause);
	}
}
